a = 10
b = 3

# Suma
suma = a + b

# Resta
resta = a - b

# Multiplicación
multiplicacion = a * b

# División
division = a / b  # División decimal
division_entera = a // b  # División entera

# Potencia
potencia = a ** b

# Módulo (resto de la división)
modulo = a % b

print(suma, resta, multiplicacion, division, division_entera, potencia, modulo)
